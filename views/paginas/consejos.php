    <h1 class="tituloC">
        ¿Qué tipos de mantenimiento pueden requerir los autos?
    </h1>
    <p>
        <span class="span">Mantenimiento correctivo: </span>Consiste en la reparación de diferentes partes del vehículo 
        cuando dejan de funcionar o comienzan a fallar.
    </p>

    <p>
        <span class="span">Mantenimiento preventivo: </span>Lo realizamos según las instrucciones del fabricante, las
        cuales indican los espacios de tiempo o kilometraje en los que se deben sustituir ciertas partes del vehículo o revisarlas.
    </p>

    <p>
        <span class="span">Mantenimiento predictivo: </span>Es el cual predice si es necesario realizar correcciones o ajustes antes 
        de que ocurra una falla debido a diagnósticos o mediciones previas.
    </p>

    <p>Te mostramos algunos consejos para mantener tu auto en las mejores condiciones:</p>

    <h2 class="tituloC" style="text-align: left;">Mantenimiento mensual, semestral y anual</h2>
    <p>lleva tu auto a un mantenimiento rutinario aunque aparente que este en buenas condiciones, para que 
        el mecánico revise todas las partes como los frenos, las bandas, las llantas, la batería, el aceite y el 
        radiador. Y un chequeo semestral o anualmente para revisar al menos el afinamiento, las bujías, el aire acondicionado y el motor.
    </p>

    <h2 class="tituloC" style="text-align: left;">Observa tus llantas y neumáticos </h2>
    <p> revisar dos a tres veces a la semana la presión de aire de tus neumáticos y ajustarla al valor correcto para asi tener un mejor 
        manejo y un menor desgaste de ellos, tambien revisa el estado de los neumáticos para evita accidentes o multas de trancito.
    </p>

    <h2 class="tituloC" style="text-align: left;">Afina el motor </h2>
    <p> Es fundamental que cambies el aceite lubricante y reemplaces algunas de las piezas como las bujías, los filtros de aire, el 
        aceite y la gasolina, ya que estas renovaciones te permitirán evitar un bajo rendimiento.
    </p>

    
    <h2 class="tituloC" style="text-align: left;">Examinar y cambiar el aceite</h2>
    <p> El cambio de aceite debes hacerlo en los periodos que indique el manual del fabricante y no olvidar reemplazar el filtro del
        aceite, ya que un filtro sucio impide el flujo correcto del aceite y puede desgastar o dañar el motor .
    </p>

    <h2 class="tituloC" style="text-align: left;">Revisar el líquido refrigerante</h2>
    <p> Valida el nivel real de líquido refrigerante, verificar el nivel con el motor frío y si es menor el nivel del líquido
        refrigerante al que debe tener, rellenarlo hasta alcanzar el indicado. Te ayudará a asegurarte de que tu motor no se 
        sobrecaliente, y así evitarás problemas con sus componentes.
    </p>

